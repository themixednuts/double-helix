//! Shared UI model for frontend-agnostic rendering.
//!
//! Commands mutate `Model` directly. Frontends read it and render.
//! No action enum indirection — the model is the single source of truth.
//!
//! Layer and panel content uses trait objects (`Box<dyn LayerModel>`,
//! `Box<dyn PanelModel>`) instead of enums, so new model types can be
//! added without modifying this module.

