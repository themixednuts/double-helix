/// A single layer in the compositor stack.
pub struct LayerEntry {
    pub id: LayerId,
    /// Typed content model for rendering. Downcast to concrete type via
    /// `content.downcast_ref::<T>()`.
    pub content: Box<dyn ContentModel>,
    pub placement: Placement,
}

// Manual Debug for cleaner output — shows tag instead of full content.
impl Debug for LayerEntry {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.debug_struct("LayerEntry")
            .field("id", &self.id)
            .field("tag", &self.content.tag())
            .field("placement", &self.placement)
            .finish()
    }
}

/// How a layer or float is positioned.
