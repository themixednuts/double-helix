#[cfg(test)]
mod tests {
    use super::*;

    // ─── Layer lifecycle ────────────────────────────────────────────

    #[test]
    fn push_layer_assigns_unique_ids() {
        let mut ui = Model::default();
        let id1 = ui.push_layer(Box::new(PickerModel::default()), Placement::Fullscreen);
        let id2 = ui.push_layer(Box::new(PromptModel::default()), Placement::Fullscreen);
        assert_ne!(id1, id2);
        assert_eq!(ui.layers.len(), 2);
    }

    #[test]
    fn push_layer_focuses_new_layer() {
        let mut ui = Model::default();
        assert_eq!(ui.focus, FocusTarget::Editor);
        let id = ui.push_layer(Box::new(PickerModel::default()), Placement::Fullscreen);
        assert_eq!(ui.focus, FocusTarget::Layer(id));
    }

    #[test]
    fn pop_layer_restores_focus() {
        let mut ui = Model::default();
        let id1 = ui.push_layer(Box::new(PickerModel::default()), Placement::Fullscreen);
        let _id2 = ui.push_layer(Box::new(PromptModel::default()), Placement::Fullscreen);
        ui.pop_layer();
        assert_eq!(ui.focus, FocusTarget::Layer(id1));
        ui.pop_layer();
        assert_eq!(ui.focus, FocusTarget::Editor);
    }

    #[test]
    fn pop_empty_returns_none() {
        let mut ui = Model::default();
        assert!(ui.pop_layer().is_none());
    }

    #[test]
    fn remove_layer_by_id() {
        let mut ui = Model::default();
        let id1 = ui.push_layer(Box::new(PickerModel::default()), Placement::Fullscreen);
        let id2 = ui.push_layer(Box::new(PromptModel::default()), Placement::Fullscreen);
        assert!(ui.remove_layer(id1));
        assert_eq!(ui.layers.len(), 1);
        assert_eq!(ui.layers[0].id, id2);
    }

    #[test]
    fn remove_focused_layer_refocuses() {
        let mut ui = Model::default();
        let id1 = ui.push_layer(Box::new(PickerModel::default()), Placement::Fullscreen);
        let id2 = ui.push_layer(Box::new(PromptModel::default()), Placement::Fullscreen);
        assert_eq!(ui.focus, FocusTarget::Layer(id2));
        ui.remove_layer(id2);
        assert_eq!(ui.focus, FocusTarget::Layer(id1));
    }

    #[test]
    fn remove_nonexistent_layer_returns_false() {
        let mut ui = Model::default();
        assert!(!ui.remove_layer(LayerId::new(999)));
    }

    // ─── Layer model access (generic) ───────────────────────────────

    #[test]
    fn layer_model_mut_downcasts_correctly() {
        let mut ui = Model::default();
        let id = ui.push_layer(Box::new(PickerModel::default()), Placement::Fullscreen);
        let model = ui.layer_model_mut::<PickerModel>(id).unwrap();
        model.query = "test".into();
        assert_eq!(ui.layer_model_mut::<PickerModel>(id).unwrap().query, "test");
    }

    #[test]
    fn layer_model_mut_returns_none_for_wrong_type() {
        let mut ui = Model::default();
        let id = ui.push_layer(Box::new(PickerModel::default()), Placement::Fullscreen);
        assert!(ui.layer_model_mut::<PromptModel>(id).is_none());
    }

    #[test]
    fn has_layer_reflects_state() {
        let mut ui = Model::default();
        assert!(!ui.has_layer::<PickerModel>());
        let id = ui.push_layer(Box::new(PickerModel::default()), Placement::Fullscreen);
        assert!(ui.has_layer::<PickerModel>());
        ui.remove_layer(id);
        assert!(!ui.has_layer::<PickerModel>());
    }

    // ─── Panel lifecycle ────────────────────────────────────────────

    #[test]
    fn insert_and_remove_panel() {
        let mut ui = Model::default();
        let id = ui.insert_panel(
            "Test Panel",
            Box::new(BlocksPanelModel::default()),
            PanelSide::Right,
            PanelSize::Percent(30),
        );
        assert_eq!(ui.panels.len(), 1);
        assert_eq!(ui.panels[id].tag(), "blocks");
        assert_eq!(ui.panels[id].title, "Test Panel");
        assert!(ui.remove_panel(id));
        assert_eq!(ui.panels.len(), 0);
    }

    #[test]
    fn remove_focused_panel_resets_focus() {
        let mut ui = Model::default();
        let id = ui.insert_panel(
            "Agent",
            Box::new(AcpModel::default()),
            PanelSide::Right,
            PanelSize::Percent(35),
        );
        ui.focus = FocusTarget::Panel(id);
        ui.remove_panel(id);
        assert_eq!(ui.focus, FocusTarget::Editor);
    }

    #[test]
    fn panel_model_mut_downcasts_correctly() {
        let mut ui = Model::default();
        let id = ui.insert_panel(
            "Agent",
            Box::new(AcpModel::default()),
            PanelSide::Right,
            PanelSize::Percent(35),
        );
        let model = ui.panel_model_mut::<AcpModel>(id).unwrap();
        model.agent_name = "Claude Code".into();
        assert_eq!(
            ui.panel_model_mut::<AcpModel>(id).unwrap().agent_name,
            "Claude Code"
        );
    }

    // ─── Status ─────────────────────────────────────────────────────

    #[test]
    fn set_and_clear_status() {
        let mut ui = Model::default();
        assert!(ui.status.is_none());
        ui.set_status(Some(StatusMessage {
            text: "hello".into(),
            timeout_ms: Some(3000),
        }));
        assert_eq!(ui.status.as_ref().unwrap().text, "hello");
        ui.set_status(None);
        assert!(ui.status.is_none());
    }

    // ─── Multiple layers + panels coexist ───────────────────────────

    #[test]
    fn layers_and_panels_independent() {
        let mut ui = Model::default();
        let layer_id = ui.push_layer(Box::new(PickerModel::default()), Placement::Fullscreen);
        let panel_id = ui.insert_panel(
            "Files",
            Box::new(TreePanelModel::default()),
            PanelSide::Left,
            PanelSize::fixed(30),
        );
        assert_eq!(ui.layers.len(), 1);
        assert_eq!(ui.panels.len(), 1);

        ui.remove_layer(layer_id);
        assert_eq!(ui.layers.len(), 0);
        assert_eq!(ui.panels.len(), 1);

        ui.push_layer(Box::new(PromptModel::default()), Placement::Fullscreen);
        ui.remove_panel(panel_id);
        assert_eq!(ui.layers.len(), 1);
        assert_eq!(ui.panels.len(), 0);
    }
}
