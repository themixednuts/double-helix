/// Marker type for layer IDs.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum LayerKind {}

/// Stable ID for a layer. Generated by `Model::next_layer_id()`.
pub type LayerId = crate::id::Id<LayerKind>;

// ─── Layer / Panel model traits ─────────────────────────────────────────

/// Trait for layer content models. Implemented by any type that can be
/// stored as layer content in `Model`. Uses `Any` for downcasting to
/// concrete types at render time.
pub trait LayerModel: Debug + Send + Sync + Any {
    fn as_any(&self) -> &dyn Any;
    fn as_any_mut(&mut self) -> &mut dyn Any;
    /// Identifier for this model type (e.g. "picker", "prompt").
    fn tag(&self) -> &str;
}

/// Trait for panel content models. Same pattern as `LayerModel`.
pub trait PanelModel: Debug + Send + Sync + Any {
    fn as_any(&self) -> &dyn Any;
    fn as_any_mut(&mut self) -> &mut dyn Any;
    /// Identifier for this model type (e.g. "chat", "list", "tree").
    fn tag(&self) -> &str;
}

/// Implement `LayerModel` for a concrete type with a static tag.
#[macro_export]
macro_rules! impl_layer_model {
    ($ty:ty, $tag:expr) => {
        impl $crate::model::LayerModel for $ty {
            fn as_any(&self) -> &dyn ::std::any::Any {
                self
            }
            fn as_any_mut(&mut self) -> &mut dyn ::std::any::Any {
                self
            }
            fn tag(&self) -> &str {
                $tag
            }
        }
    };
}

/// Implement `PanelModel` for a concrete type with a static tag.
#[macro_export]
macro_rules! impl_panel_model {
    ($ty:ty, $tag:expr) => {
        impl $crate::model::PanelModel for $ty {
            fn as_any(&self) -> &dyn ::std::any::Any {
                self
            }
            fn as_any_mut(&mut self) -> &mut dyn ::std::any::Any {
                self
            }
            fn tag(&self) -> &str {
                $tag
            }
        }
    };
}

