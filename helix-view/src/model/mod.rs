    #[test]
    fn focus_history_push_pop() {
        let mut ui = Model::default();
        let panel_id = ui.insert_panel(
            "Agent",
            Box::new(AssistantModel::default()),
            PanelSide::Right,
            PanelSize::Percent(35),
        );
        assert_eq!(ui.focus, FocusTarget::Editor);

        ui.push_focus(FocusTarget::Panel(panel_id));
        assert_eq!(ui.focus, FocusTarget::Panel(panel_id));

        let float_id = ui.create_float(
            Box::new(TextFloatModel::default()),
            Placement::Centered {
                width: 40,
                height: 10,
            },
            None,
            false,
            true,
        );
        assert_eq!(ui.focus, FocusTarget::Float(float_id));

        ui.pop_focus();
        assert_eq!(ui.focus, FocusTarget::Panel(panel_id));

        ui.pop_focus();
        assert_eq!(ui.focus, FocusTarget::Editor);

        // Popping empty history goes to Editor
        ui.pop_focus();
        assert_eq!(ui.focus, FocusTarget::Editor);
    }

