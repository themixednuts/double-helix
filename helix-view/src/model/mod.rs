// ─── Content model trait ───────────────────────────────────────────────

/// Unified trait for all UI content models (layers, panels, floats).
///
/// Any type stored as content in a `LayerEntry`, `PanelEntry`, or
/// `FloatEntry` implements this trait. The `tag()` method provides a
/// stable string identifier for the model type (e.g. "picker", "assistant-panel",
/// "text").
///
/// Downcasting to concrete types uses Rust's trait upcasting (stable since
/// 1.76): `dyn ContentModel` can be coerced to `&dyn Any`, so no manual
/// `as_any()` boilerplate is needed. Convenience methods `downcast_ref`,
/// `downcast_mut`, and `is` are provided as inherent methods on
/// `dyn ContentModel`.
pub trait ContentModel: Debug + Send + Sync + Any {
    /// Stable identifier for this model type. Used for matching in
    /// event routing and debugging (e.g. "picker", "assistant-panel", "text").
    fn tag(&self) -> &str;
}

/// Inherent methods on `dyn ContentModel` for type-safe downcasting.
///
/// These leverage trait upcasting (`dyn ContentModel` → `dyn Any`) so
/// implementations don't need manual `as_any()` methods.
impl dyn ContentModel {
    /// Downcast to a concrete model type by reference.
    pub fn downcast_ref<T: 'static>(&self) -> Option<&T> {
        (self as &dyn Any).downcast_ref()
    }

    /// Downcast to a concrete model type by mutable reference.
    pub fn downcast_mut<T: 'static>(&mut self) -> Option<&mut T> {
        (self as &mut dyn Any).downcast_mut()
    }

    /// Check whether this model is a specific concrete type.
    pub fn is<T: 'static>(&self) -> bool {
        (self as &dyn Any).is::<T>()
    }
}

/// Implement `ContentModel` for a concrete type with a static tag.
///
/// ```ignore
/// crate::impl_content_model!(PickerModel, "picker");
/// ```
#[macro_export]
macro_rules! impl_content_model {
    ($ty:ty, $tag:expr) => {
        impl $crate::model::ContentModel for $ty {
            fn tag(&self) -> &str {
                $tag
            }
        }
    };
}

