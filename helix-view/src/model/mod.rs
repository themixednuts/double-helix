/// Plugin panel model — lightweight metadata for a Lua-rendered panel.
///
/// The actual rendering is done by the Lua callback; this model just holds
/// the metadata needed for the compositor to allocate space and for the
/// PluginPanel Component to find its callbacks.
#[derive(Debug, Clone)]
pub struct PluginPanelModel {
    pub plugin_name: String,
    pub panel_id: String,
    pub render_callback_id: u64,
    pub event_callback_id: Option<u64>,
}

#[derive(Debug, Clone)]
pub struct StatusMessage {
    pub text: String,
    pub timeout_ms: Option<u64>,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum FocusTarget {
    Editor,
    Layer(LayerId),
    Panel(PanelId),
}

