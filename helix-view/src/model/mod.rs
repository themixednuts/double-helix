/// Root UI state. Commands mutate this; frontends render it.
#[derive(Debug)]
pub struct Model {
    /// Ordered layer stack (bottom to top), rendered in order.
    pub layers: Vec<LayerEntry>,

    /// Persistent side panels (assistant, file tree, diagnostics, etc.).
    /// SlotMap for stable IDs — panels can be added/removed without index shifts.
    pub panels: SlotMap<PanelId, PanelEntry>,

    /// Persistent floating windows. Rendered above panels, below modal layers.
    pub floats: SlotMap<FloatId, FloatEntry>,

    /// Status line / transient notifications.
    pub status: Option<StatusMessage>,

    /// What currently has input focus.
    pub focus: FocusTarget,

    /// Focus history stack for returning after dismissals. Bounded to 16 entries.
    focus_history: Vec<FocusTarget>,

    /// Counter for allocating unique layer IDs.
    next_layer_id: usize,
}

impl Default for Model {
    fn default() -> Self {
        Self {
            layers: Vec::new(),
            panels: SlotMap::default(),
            floats: SlotMap::default(),
            status: None,
            focus: FocusTarget::Editor,
            focus_history: Vec::new(),
            next_layer_id: 0,
        }
    }
}

impl Model {
    /// Allocate a new layer ID. Call when pushing a layer.
    pub fn next_layer_id(&mut self) -> LayerId {
        let id = LayerId::new(self.next_layer_id);
        self.next_layer_id = self.next_layer_id.saturating_add(1);
        id
    }

    /// Push a new layer onto the stack and focus it. Returns its `LayerId`.
    pub fn push_layer(&mut self, content: Box<dyn ContentModel>, placement: Placement) -> LayerId {
        let id = self.next_layer_id();
        self.layers.push(LayerEntry {
            id,
            content,
            placement,
        });
        self.focus = FocusTarget::Layer(id);
        id
    }

    /// Remove a layer by ID. Returns `true` if found. Resets focus to editor
    /// if the removed layer was focused.
    pub fn remove_layer(&mut self, id: LayerId) -> bool {
        let was_focused = self.focus == FocusTarget::Layer(id);
        let idx = self.layers.iter().position(|l| l.id == id);
        if let Some(idx) = idx {
            self.layers.remove(idx);
            if was_focused {
                self.focus = self
                    .layers
                    .last()
                    .map(|l| FocusTarget::Layer(l.id))
                    .unwrap_or(FocusTarget::Editor);
            }
            true
        } else {
            false
        }
    }

    /// Pop the topmost layer. Returns its entry if the stack was non-empty.
    pub fn pop_layer(&mut self) -> Option<LayerEntry> {
        let entry = self.layers.pop();
        if let Some(ref e) = entry {
            if self.focus == FocusTarget::Layer(e.id) {
                self.focus = self
                    .layers
                    .last()
                    .map(|l| FocusTarget::Layer(l.id))
                    .unwrap_or(FocusTarget::Editor);
            }
        }
        entry
    }

    /// Get a reference to the topmost layer, if any.
    pub fn top_layer(&self) -> Option<&LayerEntry> {
        self.layers.last()
    }

    /// Get a mutable reference to a layer by ID.
    pub fn layer_mut(&mut self, id: LayerId) -> Option<&mut LayerEntry> {
        self.layers.iter_mut().find(|l| l.id == id)
    }

    /// Downcast a layer's content to a concrete model type.
    pub fn layer_model_mut<M: 'static>(&mut self, id: LayerId) -> Option<&mut M> {
        self.layer_mut(id)
            .and_then(|l| l.content.downcast_mut::<M>())
    }

    /// Check if any layer contains a model of type `M`.
    pub fn has_layer<M: 'static>(&self) -> bool {
        self.layers.iter().any(|l| l.content.is::<M>())
    }

    /// Insert a panel. Returns its `PanelId`.
    pub fn insert_panel(
        &mut self,
        title: impl Into<String>,
        content: Box<dyn ContentModel>,
        side: PanelSide,
        size: PanelSize,
    ) -> PanelId {
        let title = title.into();
        self.panels.insert_with_key(|id| PanelEntry {
            id,
            title,
            content,
            side,
            size,
            visible: true,
        })
    }

    /// Downcast a panel's content to a concrete model type.
    pub fn panel_model_mut<M: 'static>(&mut self, id: PanelId) -> Option<&mut M> {
        self.panels
            .get_mut(id)
            .and_then(|p| p.content.downcast_mut::<M>())
    }

    /// Remove a panel by ID. Returns `true` if found.
    pub fn remove_panel(&mut self, id: PanelId) -> bool {
        if self.focus == FocusTarget::Panel(id) {
            self.pop_focus();
        }
        self.panels.remove(id).is_some()
    }

    /// Toggle a panel's visibility. Returns the new visibility state.
    pub fn toggle_panel(&mut self, id: PanelId) -> Option<bool> {
        let panel = self.panels.get_mut(id)?;
        panel.visible = !panel.visible;
        let visible = panel.visible;
        // If hiding and it was focused, return focus
        if !visible && self.focus == FocusTarget::Panel(id) {
            self.pop_focus();
        }
        Some(visible)
    }

    /// Focus a panel. Pushes the current focus onto the history stack.
    pub fn focus_panel(&mut self, id: PanelId) {
        if let Some(panel) = self.panels.get(id) {
            if panel.visible {
                self.push_focus(FocusTarget::Panel(id));
            }
        }
    }

    // ─── Float management ──────────────────────────────────────────────

    /// Create a floating window. Returns its `FloatId`.
    pub fn create_float(
        &mut self,
        content: Box<dyn ContentModel>,
        placement: Placement,
        title: Option<String>,
        dismissible: bool,
        focus: bool,
    ) -> FloatId {
        let id = self.floats.insert_with_key(|id| FloatEntry {
            id,
            title,
            content,
            placement,
            dismissible,
            owner: None,
        });
        if focus {
            self.push_focus(FocusTarget::Float(id));
        }
        id
    }

    /// Get a reference to a float by ID.
    pub fn float(&self, id: FloatId) -> Option<&FloatEntry> {
        self.floats.get(id)
    }

    /// Get a mutable reference to a float by ID.
    pub fn float_mut(&mut self, id: FloatId) -> Option<&mut FloatEntry> {
        self.floats.get_mut(id)
    }

    /// Downcast a float's content to a concrete model type.
    pub fn float_model_mut<M: 'static>(&mut self, id: FloatId) -> Option<&mut M> {
        self.floats
            .get_mut(id)
            .and_then(|f| f.content.downcast_mut::<M>())
    }

    /// Close a float by ID. Returns `true` if found.
    pub fn close_float(&mut self, id: FloatId) -> bool {
        if self.focus == FocusTarget::Float(id) {
            self.pop_focus();
        }
        self.floats.remove(id).is_some()
    }

    /// Remove all floats owned by a specific plugin.
    pub fn remove_floats_by_owner(&mut self, owner: &str) {
        let ids: Vec<FloatId> = self
            .floats
            .iter()
            .filter(|(_, f)| f.owner.as_deref() == Some(owner))
            .map(|(id, _)| id)
            .collect();
        for id in ids {
            self.close_float(id);
        }
    }

    // ─── Focus management ──────────────────────────────────────────────

    /// Push the current focus onto the history stack and set a new focus target.
    pub fn push_focus(&mut self, target: FocusTarget) {
        if self.focus != target {
            self.focus_history.push(self.focus);
            // Bound the history stack
            if self.focus_history.len() > 16 {
                self.focus_history.remove(0);
            }
            self.focus = target;
        }
    }

    /// Pop the focus history, restoring the previous focus target.
    pub fn pop_focus(&mut self) {
        self.focus = self.focus_history.pop().unwrap_or(FocusTarget::Editor);
    }

    // ─── Status & queries ──────────────────────────────────────────────

    /// Set the status message. Pass `None` to clear.
    pub fn set_status(&mut self, msg: Option<StatusMessage>) {
        self.status = msg;
    }

    /// Find the first layer matching a predicate.
    pub fn find_layer(&self, predicate: impl Fn(&LayerEntry) -> bool) -> Option<&LayerEntry> {
        self.layers.iter().find(|l| predicate(l))
    }
}

